package com.chen.leetcode.utils;

/**
 * ListNode
 *
 * @author chenbjf
 * @since 2025-03-12 09:10
 */
public class ListNode {

    int val;

    ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
